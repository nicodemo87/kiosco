    private void reloadUsers(){
        DefaultTableModel dtm = new NoEditableTableModel();
        String header[] = new String[]{"ID", "Nombre", "Permisos"};
        dtm.setColumnIdentifiers(header);
        jTable1.setModel(dtm);

        List<User> users = this.usersController.getUsers();

        users.stream().forEach((user) -> {
            dtm.addRow(new Object[]{
                user.getId(),
                user.getName(),
                user.getPermissions().stream().map((p)->p.getDescription()).collect(Collectors.joining(", "))
            }); });
    }

