    private void jbutton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbutton_SearchActionPerformed
        List<CashBox> chasBoxes = cashBoxesDAO.getAllByDateAndUser(dateChooserCombo_since.getSelectedDate().getTime(),
                dateChooserCombo_to.getSelectedDate().getTime(),
                (User) jComboBox_users.getSelectedItem());
        DefaultTableModel dtm = new NoEditableTableModel();

        String header[];
        header = new String[]{"Apertura", "Cierre", "Usuario",
            "Inicial", "Vendido", "Fiado", "D. Pagadas", "Final",
            "Costos", "Ganancias" };

        dtm.setColumnIdentifiers(header);
        jTable1.setModel(dtm);

        chasBoxes.stream().forEach((c) -> {
            dtm.addRow(new Object[]{
                c.getStartDateTime().toString(),
                c.getEndDateTime().toString(),
                c.getUser().getName(),
                c.getStartAmount(),
                c.sold(),
                c.debited(),
                c.payments(),
                c.getEndAmount(),
                c.costs(),
                c.profit()
            });
        });
    }//GEN-LAST:event_jbutton_SearchActionPerformed

