    private User getSelectedUser() {
        User user = null;
        int rowIndex = jTable1.getSelectedRow();
        if (rowIndex >= 0) {
            try {
                int id = Integer.parseInt(jTable1.getModel().getValueAt(rowIndex, 0).toString());
                user = usersController.getUsers().stream().filter((u)->u.getId() == id).findFirst().get();
            } catch (Exception  ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe elegir un usuario de la lista");
        }
        return user;
    }

